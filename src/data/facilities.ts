export const getFacilitiesByFloor = (floorId: string): Facility[] => {
  return facilities.filter((f) => f.floorId === floorId);
};

export const getFacilitiesByType = (type: FacilityType): Facility[] => {
  return facilities.filter((f) => f.type === type);
};

export const getFacilityById = (id: string): Facility | undefined => {
  return facilities.find((f) => f.id === id);
};
